#include "3-calc.h"

/**
 * op_add - sum
 * 
 * Return: a plus b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract
 * 
 * Return: a minus b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply
 * 
 * Return: a x b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * 
 * Return: divide a b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *  op_mod - modulo (take the remainder of the division)
 * 
 * Return: modulo a b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
