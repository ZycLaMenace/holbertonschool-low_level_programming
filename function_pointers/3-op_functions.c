#include "3-calc.h"

/*op_add - sum */
int op_add(int a, int b)
{
	return (a + b);
}

/* op_sub - substract */
int op_sub(int a, int b)
{
	return (a - b);
}

/* op_mul - multiply */
int op_mul(int a, int b)
{
	return (a * b);
}

/* op_div - division */
int op_div(int a, int b)
{
	return (a / b);
}

/* op_mod - modulo (take the remainder of the division)*/
int op_mod(int a, int b)
{
	return (a % b);
}
