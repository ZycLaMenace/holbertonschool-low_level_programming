#include "3-calc.h"

/* sum */
int op_add(int a, int b)
{
	return (a + b);
}

/* substract */
int op_sub(int a, int b)
{
	return (a - b);
}

/* multiply */
int op_mul(int a, int b)
{
	return (a * b);
}

/* division */
int op_div(int a, int b)
{
	return (a / b);
}

/*modulo (take the remainder of the division)*/
int op_mod(int a, int b)
{
	return (a % b);
}
